#São operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variavel.
#Atribuição Simples
saldo = 500
print(saldo)

#Atribuição com adição
saldo = 500
saldo +=200

print(saldo)

#Atruibuição com divisão normal
saldo = 500
saldo /=5
print(saldo)

#Atruibuição com divisão inteira
saldo = 500
saldo //=5
print(saldo)

#Atruibuição com Modulo(resto da divisão)
saldo = 500
saldo %=480
print(saldo)

#Atruibuição com exponenciação
saldo = 80
saldo **= 2
print(saldo)


